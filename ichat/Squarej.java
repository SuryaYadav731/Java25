package com.ichat;

public class Squarej {

        public static void main(String[] args) {
            int[] nu= {2, 3, 4};
            for (int n : nu) {
                int squ = n * n;
                int cub = n * n * n;
                System.out.println(squ);
                System.out.println(cub);
            }
        }
    }


